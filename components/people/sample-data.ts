function source(
  provider: Channel,
  externalId: string,
  collectedAt: string,
  confidence = 1,
): SourceProvenance {
  return {
    provider,
    externalId,
    collectedAt,
    confidence,
    metadata: { access: "read-only" },
  };
}

function evidence(
  id: string,
  kind: EvidenceKind,
  label: string,
  detail: string,
  observedAt: string,
  confidence: number,
  channel?: Channel,
): EvidenceView {
  return {
    id,
    kind,
    label,
    detail,
    observedAt,
    confidence,
    ...(channel ? { channel } : {}),
  };
}

function resolved(
  value: string,
  kind: EvidenceKind,
  confidence: number,
  id: string,
  detail: string,
  observedAt: string,
  channel?: Channel,
): ResolvedField {
  return {
    value,
    kind,
    confidence,
    evidence: [
      evidence(
        id,
        kind,
        kind === "inferred"
          ? "Model extraction"
          : kind === "override"
            ? "Owner correction"
            : "Source profile",
        detail,
        observedAt,
        confidence,
        channel,
      ),
    ],
    overrides:
      kind === "override"
        ? [
            {
              field: id,
              value,
              reason: "Corrected after reviewing source context.",
              overriddenAt: observedAt,
              overriddenBy: "owner@threadline.local",
            },
          ]
        : [],
  };
}

function audit(
  id: string,
  occurredAt: string,
  action: string,
  detail: string,
): AuditEntryView {
  return {
    id,
    occurredAt,
    actor: "owner@threadline.local",
    action,
    detail,
    outcome: "success",
  };
}

function timeline(
  id: string,
  happenedAt: string,
  channel: TimelineItem["channel"],
  direction: TimelineItem["direction"],
  kind: TimelineItem["kind"],
  title: string,
  summary: string,
  replyState: TimelineItem["replyState"],
  evidenceKind: EvidenceKind = "observed",
): TimelineItem {
  return {
    id,
    happenedAt,
    channel,
    direction,
    kind,
    title,
    summary,
    replyState,
    provenance: evidence(
      `${id}-source`,
      evidenceKind,
      channel === "internal" ? "Threadline record" : `${channel} source`,
      channel === "internal"
        ? "Stored only inside Threadline."
        : "Mirrored with read-only access.",
      happenedAt,
      evidenceKind === "inferred" ? 0.82 : 1,
      channel === "internal" ? undefined : channel,
    ),
  };
}

